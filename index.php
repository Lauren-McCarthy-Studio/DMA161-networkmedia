          <br><br>
          <span class='label'>1: Online identity.</span>
          How do you represent yourself online? What can you learn about yourself and others by following information trails? You will create a portfolio site or website that represents your Internet identity.
          <br><br>
          <span class='label'>2: Location, space, mobility.</span>
          Where is the Internet? How do we exist in relation to the network? You will create a mobile experience that is distributed over space and time.
          <br><br>
          <span class='label'>3: Social networks and crowds.</span>
          How does the internet connect us and allow us to socialize, to express opinions, to do work? You will create a multimedia web project using crowdsourced material.
